export const deleteWsProductsNotNormal = async () => {
  const wholesaleProducts = await tnWholesaleStore.getAllProducts({
    includeVirtualProducts: true,
    includeGiftCards: true,
  });

  let deteledProductsCount = 0;
  for (const product of wholesaleProducts) {
    const sku = product.variants[0].sku;
    if (!sku) {
      console.log(`SKU not found for product ${product.id}`);
      continue;
    }
    if (codes.includes(sku)) {
      console.log(`Product ${product.id} is in the list. Deleting...`);
      await tnWholesaleStore.deleteProduct(product.id);
      deteledProductsCount++;
    }
  }

  console.log("Script complete!");
  console.log(`Deleted ${deteledProductsCount} products`);
};
